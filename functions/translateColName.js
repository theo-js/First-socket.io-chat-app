exports.fr = color => {
	switch(color) {
		case 'blue':
			return 'bleu'
			break
		case 'cyan':
			return 'cyan'
			break
		case 'crimson':
			return 'rouge'
			break
		case 'pink':
			return 'rose'
			break
		case 'purple':
			return 'violet'
			break
		case 'orange':
			return 'orange'
			break
		case 'lightgrey':
			return 'gris'
			break
		case 'lightgreen':
			return 'vert'
			break
		case 'yellow':
			return 'jaune'
			break
		default:
			return ''
			break
	}
}